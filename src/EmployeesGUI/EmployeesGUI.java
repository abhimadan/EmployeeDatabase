    /**
     * This method generates a random employee number from 0 to 999999; if the
     * generated number is already being used as an employee number, it will
     * cycle through the range until a number not owned by another employee is
     * found. Returns a string because this value is assigned as the text in a
     * text box.
     */
    private String randomNumGenerator() {
        int numGenerated = (new Random().nextInt(1000000));
        while (usedEmpNumbers.contains(numGenerated)) {
            if (numGenerated == 999999) {
                numGenerated = 1;
            } else {
                numGenerated++;
            }
        }
        return String.valueOf(numGenerated);
    }

    /**
     * This method reads the backup file with all the employee information from
     * the last usage of the database, and then stores this information into the
     * hash table.
     */
    private static void load() {
        try {
            FileInputStream fstream = new FileInputStream("employees.txt"); //open file
            BufferedReader br = new BufferedReader(new InputStreamReader(fstream)); // create bufferedreader to read data

            int numEmployees = Integer.parseInt(br.readLine());
            br.readLine();

            for (int i = 0; i < numEmployees; i++) {
                Employee list;
                if (br.readLine().equals("PTE")) {

                    list = new PartTimeEmployee(br.readLine(), br.readLine(), Integer.parseInt(br.readLine()), br.readLine().charAt(0), Double.parseDouble(br.readLine()), Double.parseDouble(br.readLine()), Double.parseDouble(br.readLine()), Integer.parseInt(br.readLine())); //instantiate memory to store data
                    table.addEmployee(list);
                    br.readLine(); // get empty space and discard it
                } else {
                    list = new FullTimeEmployee(br.readLine(), br.readLine(), Integer.parseInt(br.readLine()), br.readLine().charAt(0), Double.parseDouble(br.readLine()), Double.parseDouble(br.readLine()));
                    table.addEmployee(list);
                    br.readLine();
                }
            }

            br.close(); //close the file
            fstream.close();

        } catch (IOException | NumberFormatException e) { // catch exception if  there is error reading
            System.err.println("Error: " + e.getMessage());
        }
    }


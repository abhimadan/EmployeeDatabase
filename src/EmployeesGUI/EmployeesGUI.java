    /**
     * This method is called when the Add button in the Add Full Time Employee tab
     * is clicked. It checks if the user input is valid in each field; if not, an 
     * appropriate error message will be shown. If all fields have valid information,
     * the employee is added to the database, and the user is given a message stating
     * that the employee was added successfully.
     */
    private void AddButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButton1ActionPerformed
        boolean yes = true;
        if (fname1.getText().equals("")) {
            jLabel31.setVisible(true);
            yes = false;
        } else {
            jLabel31.setVisible(false);
        }

        if (lname1.getText().equals("")) {
            jLabel32.setVisible(true);
            yes = false;
        } else {
            jLabel32.setVisible(false);
        }

        if (!Male1.isSelected() && !Female1.isSelected()) {
            jLabel33.setVisible(true);
            yes = false;
        } else {
            jLabel33.setVisible(false);
        }

        if (Wage1.getText().equals("") || Wage1.getText().equals("0.00") || Wage1.getText().charAt(0) == '-') {
            jLabel34.setVisible(true);
            yes = false;
        } else {
            jLabel34.setVisible(false);
        }

        if (yes == true) {
            jLabel35.setVisible(false);
            jLabel36.setVisible(false);
            jLabel37.setVisible(false);
            Employee list;
            String fn = fname1.getText();
            String ln = lname1.getText();
            int id = Integer.parseInt(GenID1.getText());
            double ded = (Double) Deductions1.getValue();
            double wage = Double.parseDouble(Wage1.getText());
            char g;
            if (Male1.isSelected()) {
                g = 'M';
            } else {
                g = 'F';
            }
            list = new FullTimeEmployee(fn, ln, id, g, ded, wage);
            table.addEmployee(list);
            usedEmpNumbers.add(id);

            fname1.setText("");
            lname1.setText("");
            Wage1.setText("");
            Deductions1.setValue(0.0);
            MaleFemale.clearSelection();
            jLabel36.setText(GenID1.getText());
            GenID1.setText(randomNumGenerator());
            jLabel35.setVisible(true);
            jLabel36.setVisible(true);
            jLabel37.setVisible(true);
        }
    }//GEN-LAST:event_AddButton1ActionPerformed


    /**
     * This method is called when the Add button in the Add Part Time Employee tab
     * is clicked. It checks if the user input is valid in each field; if not, an 
     * appropriate error message will be shown. If all fields have valid information,
     * the employee is added to the database, and the user is given a message stating
     * that the employee was added successfully.
     */
    private void AddButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddButtonActionPerformed
        boolean yes = true;
        if (fname.getText().equals("")) {
            jLabel25.setVisible(true);
            yes = false;
        } else {
            jLabel25.setVisible(false);
        }

        if (lname.getText().equals("")) {
            jLabel26.setVisible(true);
            yes = false;
        } else {
            jLabel26.setVisible(false);
        }

        if (!Male.isSelected() && !Female.isSelected()) {
            jLabel28.setVisible(true);
            yes = false;
        } else {
            jLabel28.setVisible(false);
        }

        if (Wage.getText().equals("") || Wage.getText().equals("0.00") || Wage.getText().charAt(0) == '-') {
            jLabel27.setVisible(true);
            yes = false;
        } else {
            jLabel27.setVisible(false);
        }
        if ((Double) HPW.getValue() == 0) {
            jLabel29.setVisible(true);
            yes = false;
        } else {
            jLabel29.setVisible(false);
        }

        if ((Integer) WPY.getValue() == 0) {
            jLabel30.setVisible(true);
            yes = false;
        } else {
            jLabel30.setVisible(false);
        }

        if (yes == true) {
            Employee list;
            String fn = fname.getText();
            String ln = lname.getText();
            int id = Integer.parseInt(GenID.getText());
            double ded = (Double) Deductions.getValue();
            double wage = Double.parseDouble(Wage.getText());
            double hpw = (Double) HPW.getValue();
            int wpy = (Integer) WPY.getValue();
            char g;
            if (Male.isSelected()) {
                g = 'M';
            } else {
                g = 'F';
            }
            list = new PartTimeEmployee(fn, ln, id, g, ded, wage, hpw, wpy);
            table.addEmployee(list);
            usedEmpNumbers.add(id);

            fname.setText("");
            lname.setText("");
            Wage.setText("");
            MaleFemale.clearSelection();
            HPW.setValue(0.0);
            WPY.setValue(0);
            Deductions.setValue(0.0);
            jLabel23.setText(GenID.getText());
            GenID.setText(randomNumGenerator());
            jLabel22.setVisible(true);
            jLabel23.setVisible(true);
            jLabel24.setVisible(true);
        }

    }//GEN-LAST:event_AddButtonActionPerformed


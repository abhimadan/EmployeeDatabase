    /**
     * This method is called when the program is opened. It writes the user guide 
     * (text-only version) from a text file to the text area in the Help panel.
     */
    private void writeToTextArea(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_writeToTextArea
        try {
            FileInputStream fstream = new FileInputStream("help.txt");
            BufferedReader br = new BufferedReader(new InputStreamReader(fstream));

            String write = br.readLine();
            jTextArea1.setText(write + '\n');

            while (br.ready()) {
                write = br.readLine();
                jTextArea1.append(write + '\n');
            }

            br.close();
            fstream.close();
            jTextArea1.setCaretPosition (0); //puts cursor at top of panel
            
        } catch (IOException e) { // catch exception if  there is error reading
            System.err.println("Error: " + e.getMessage());
        }
    }//GEN-LAST:event_writeToTextArea


    /**
     * This method is called when the Enter button is pressed and the search field
     * is selected. It then uses the employee number given in the search field and
     * searches for an employee with that employee number in the hash table. If 
     * the text provided in the search field is not a number, an error message will
     * be shown. If an employee number given does not belong to any employee in
     * the database, the user will be informed that an employee with that number
     * was not found in the database. If an employee with a matching employee 
     * number is found, their information will be displayed to the screen.
     */
    private void SearchFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchFieldActionPerformed
        boolean yes = true;
        try {
            Integer.parseInt(SearchField.getText());
        } catch (NumberFormatException e) {
            yes = false;
        }

        if (yes && Integer.parseInt(SearchField.getText()) >= 0) { //if the entry is not a number

            Employee selected = table.getEmployee(Integer.parseInt(SearchField.getText()));
            if (selected != null) {    //employee was found in the database

                if (selected instanceof PartTimeEmployee) {
                    showpanel(P5);
                    jLabel39.setVisible(false);
                    jLabel40.setVisible(false);
                    jLabel42.setVisible(false);
                    jLabel43.setVisible(false);
                    jLabel44.setVisible(false);
                    jLabel41.setVisible(false);
                    fname2.setText(selected.getFirstName());
                    lname2.setText(selected.getLastName());
                    GenID2.setText(String.valueOf(selected.getEmpNumber()));
                    if (selected.getSex() == 'M') {
                        Male2.setSelected(true);
                    } else {
                        Female2.setSelected(true);
                    }
                    Wage2.setText(String.valueOf(((PartTimeEmployee) selected).getHourlyWage()));
                    HPW1.setValue(((PartTimeEmployee) selected).getHrsPerWeek());
                    WPY1.setValue(((PartTimeEmployee) selected).getWeeksPerYear());
                    Deductions2.setValue(selected.getDeductionsRate());
                    GAP.setText(((PartTimeEmployee) selected).getGrossAnnualPay());
                    NAP.setText(((PartTimeEmployee) selected).getNetAnnualPay());

                } else if (selected instanceof FullTimeEmployee) {
                    showpanel(P6);
                    jLabel50.setVisible(false);
                    jLabel51.setVisible(false);
                    jLabel53.setVisible(false);
                    jLabel52.setVisible(false);
                    fname3.setText(selected.getFirstName());
                    lname3.setText(selected.getLastName());
                    GenID3.setText(String.valueOf(selected.getEmpNumber()));
                    if (selected.getSex() == 'M') {
                        Male3.setSelected(true);
                    } else {
                        Female3.setSelected(true);
                    }
                    Wage3.setText(String.valueOf(((FullTimeEmployee) selected).getAnnualSalary()));
                    Deductions3.setValue(selected.getDeductionsRate());
                    GAP1.setText(((FullTimeEmployee) selected).getGrossAnnualPay());
                    NAP1.setText(((FullTimeEmployee) selected).getNetAnnualPay());
                }
            } else {
                jOptionPane.setVisible(true);
                JOptionPane.showMessageDialog(jOptionPane, "Employee not found.", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            jOptionPane.setVisible(true);
            JOptionPane.showMessageDialog(jOptionPane, "Text must be a positive numerical value!", "Warning", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_SearchFieldActionPerformed

